char	*ft_strcat(char *ret, const char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (ret[i] != '\0')
		++i;
	while (str[j] != '\0')
		ret[i++] = str[j++];
	ret[i] = '\0';
	return (ret);
}
